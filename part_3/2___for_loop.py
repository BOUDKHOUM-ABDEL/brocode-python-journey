

for x in range(5):
    print(x)  # prints 0, 1, 2, 3, 4 on separate lines

for x in range(5):
    print(x ,end=" ")  # prints 0, 1, 2, 3, 4 on the same lines

for x in range(2, 5):
    print(x)  # prints 2, 3, 4 on separate lines

for x in range(2, 10, 3):
    print(x)  # prints 2, 5, 8 on separate lines

for x in range(1,5):
    print(x * '*')  # prints a triangle of asterisks
    

